DROP TABLE EJB_Relation_OTM_Order;
DROP TABLE EJB_Relation_OTM_OrderItem;
CREATE TABLE EJB_Relation_OTM_Order
(
  ORDER_ID NUMBER(10, 0) NOT NULL,
  AMOUNT FLOAT(126),
  CREATE_DATE TIMESTAMP(6)
, PRIMARY KEY(ORDER_ID)
);
