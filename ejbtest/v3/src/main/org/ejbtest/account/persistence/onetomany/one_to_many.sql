(
  Order_Item_ID NUMBER(10, 0) NOT NULL,
  PRODUCTNAME VARCHAR2(255 CHAR),
  PRICE FLOAT(126),
  ORDER_ID NUMBER(10, 0) NOT NULL
, PRIMARY KEY( ID  )
, FOREIGN KEY( ORDER_ID) REFERENCES EJB_OneToMany_Order(ORDER_ID) ENABLE
);
